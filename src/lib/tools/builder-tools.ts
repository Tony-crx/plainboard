/**
 * Project Scaffolder — generate complete project structures from templates
 */
export const projectScaffolderTool: Tool = {
  type: 'function',
  function: {
    name: 'scaffold_project',
    description: 'Generate complete project structures: Next.js app, Express API, Python Flask, CLI tool, library, or custom. Creates all files, configs, and boilerplate.',
    parameters: {
      type: 'object',
      properties: {
        type: {
          type: 'string',
          enum: ['nextjs', 'express-api', 'fastapi', 'cli', 'library', 'react-vite', 'custom'],
          description: 'Project type to scaffold'
        },
        name: {
          type: 'string',
          description: 'Project name'
        },
        dir: {
          type: 'string',
          description: 'Parent directory for the project (default: sandbox)'
        },
        extras: {
          type: 'string',
          description: 'Extra features to include: "auth,db,tests,docker,ci" (comma-separated)'
        }
      },
      required: ['type', 'name']
    }
  },
  execute: async ({ type, name, dir = '', extras = '' }: {
    type: string; name: string; dir?: string; extras?: string
  }) => {
    try {
      const SAFE_DIR = process.env.SAFE_DIR || path.join(/*turbopackIgnore: true*/ process.cwd(), 'sandbox');
      const baseDir = dir ? path.join(/*turbopackIgnore: true*/ SAFE_DIR, dir) : SAFE_DIR;
      const projectDir = path.join(baseDir, name);
      const features = extras ? extras.split(',').map(f => f.trim()) : [];

      // Generate file tree based on project type
      const fileTree: Record<string, string> = {};

      switch (type) {
        case 'nextjs':
          Object.assign(fileTree, generateNextjsProject(name, features));
          break;
        case 'express-api':
          Object.assign(fileTree, generateExpressProject(name, features));
          break;
        case 'fastapi':
          Object.assign(fileTree, generateFastapiProject(name, features));
          break;
        case 'cli':
          Object.assign(fileTree, generateCliProject(name, features));
          break;
        case 'library':
          Object.assign(fileTree, generateLibraryProject(name, features));
          break;
        case 'react-vite':
          Object.assign(fileTree, generateReactViteProject(name, features));
          break;
        default:
          return `Unknown project type: ${type}. Supported: nextjs, express-api, fastapi, cli, library, react-vite, custom`;
      }

      // Write all files
      const written: string[] = [];
      for (const [relPath, content] of Object.entries(fileTree)) {
        const fullPath = path.join(projectDir, relPath);
        await fs.mkdir(path.dirname(fullPath), { recursive: true });
        await fs.writeFile(fullPath, content, 'utf-8');
        written.push(relPath);
      }

      // Generate output
      let output = `═══ ${type.toUpperCase()} PROJECT: ${name} ═══\n\n`;
      output += `📁 ${name}/\n`;
      output += renderTree(fileTree, '');
      output += `\n✅ ${written.length} files created\n`;
      output += `\nNext steps:\n`;
      output += `  cd ${name}\n`;
      output += getInstallInstructions(type, features);

      return output.trim();
    } catch (err: any) {
      return `Scaffold error: ${err.message}`;
    }
  }
};

