function renderTree(files: Record<string, string>, prefix: string): string {
  const entries = Object.keys(files).sort();
  let output = '';
  for (let i = 0; i < entries.length; i++) {
    const isLast = i === entries.length - 1;
    const connector = isLast ? '└── ' : '├── ';
    const parts = entries[i].split('/');
    const fileName = parts[parts.length - 1];
    output += `${prefix}${connector}${fileName}\n`;
  }
  return output;
}

function getInstallInstructions(type: string, features: string[]): string {
  const base = `  npm install\n`;
  switch (type) {
    case 'nextjs': return base + `  npm run dev\n\nOpen http://localhost:3000`;
    case 'express-api': return base + `  npm run dev\n\nAPI: http://localhost:3000/api`;
    case 'fastapi': return `  pip install .\n  uvicorn src.main:app --reload\n\nDocs: http://localhost:8000/docs`;
    case 'cli': return base + `  npm link\n  ${'your-cli'} greet World`;
    case 'library': return base + `  npm run build`;
    case 'react-vite': return base + `  npm run dev\n\nOpen http://localhost:5173`;
    default: return base;
  }
}

/**
 * Pipeline Builder — chain commands with conditions, retries, and parallel execution
 */
export const pipelineBuilderTool: Tool = {
  type: 'function',
  function: {
    name: 'run_pipeline',
    description: 'Execute a pipeline of commands with support for sequential/parallel execution, conditions, retries, and output passing between steps.',
    parameters: {
      type: 'object',
      properties: {
        steps: {
          type: 'string',
          description: 'Pipeline steps as JSON array. Each step: {"name": "step1", "cmd": "echo hello", "retry": 2, "condition": "optional shell condition"}'
        },
        mode: {
          type: 'string',
          enum: ['sequential', 'parallel'],
          description: 'Execution mode'
        },
        stopOnError: {
          type: 'boolean',
          description: 'Stop pipeline on first error (default: true)'
        },
        cwd: {
          type: 'string',
          description: 'Working directory for commands (default: sandbox)'
        }
      },
      required: ['steps']
    }
  },
  execute: async ({ steps, mode = 'sequential', stopOnError = true, cwd = '' }: {
    steps: string; mode?: string; stopOnError?: boolean; cwd?: string
  }) => {
    try {
      let parsedSteps: Array<{ name: string; cmd: string; retry?: number; condition?: string }>;
      try {
        parsedSteps = JSON.parse(steps);
      } catch {
        return 'Error: Invalid JSON in steps parameter';
      }

      if (!Array.isArray(parsedSteps) || parsedSteps.length === 0) {
        return 'Error: steps must be a non-empty JSON array';
      }

      const SAFE_DIR = process.env.SAFE_DIR || path.join(process.cwd(), 'sandbox');
      const workDir = cwd || SAFE_DIR;
      await fs.mkdir(workDir, { recursive: true });

      let output = `═══ PIPELINE EXECUTION ═══\n`;
      output += `Mode: ${mode}\n`;
      output += `Steps: ${parsedSteps.length}\n`;
      output += `Working Dir: ${workDir}\n`;
      output += `══════════════════════════\n\n`;

      const results: Array<{ name: string; status: 'SUCCESS' | 'FAILED' | 'SKIPPED'; output?: string; duration?: number }> = [];

      if (mode === 'sequential') {
        for (const step of parsedSteps) {
          // Check condition
          if (step.condition) {
            try {
              await execAsync(step.condition, { cwd: workDir, timeout: 5000 });
            } catch {
              results.push({ name: step.name, status: 'SKIPPED' });
              output += `⏭️  ${step.name}: SKIPPED (condition failed)\n`;
              continue;
            }
          }

          const maxAttempts = (step.retry || 0) + 1;
          let success = false;
          let lastOutput = '';
          let lastDuration = 0;

          for (let attempt = 1; attempt <= maxAttempts; attempt++) {
            const start = Date.now();
            try {
              const { stdout, stderr } = await execAsync(step.cmd, { cwd: workDir, timeout: 30000, maxBuffer: 1024 * 512 });
              lastOutput = (stdout + stderr).trim() || '[no output]';
              lastDuration = Date.now() - start;
              success = true;
              break;
            } catch (err: any) {
              lastOutput = err.message;
              lastDuration = Date.now() - start;
              if (attempt < maxAttempts) {
                output += `  ⚠️  Attempt ${attempt} failed, retrying...\n`;
              }
            }
          }

          results.push({ name: step.name, status: success ? 'SUCCESS' : 'FAILED', output: lastOutput, duration: lastDuration });

          if (success) {
            output += `✅ ${step.name} (${lastDuration}ms)\n`;
            output += `   ${lastOutput.substring(0, 200)}${lastOutput.length > 200 ? '...' : ''}\n`;
          } else {
            output += `❌ ${step.name} FAILED after ${maxAttempts} attempt(s)\n`;
            output += `   ${lastOutput.substring(0, 300)}\n`;
            if (stopOnError) {
              output += `\n🛑 Pipeline stopped due to stopOnError=true\n`;
              break;
            }
          }
        }
      } else {
        // Parallel mode
        const promises = parsedSteps.map(async (step) => {
          const start = Date.now();
          try {
            const { stdout, stderr } = await execAsync(step.cmd, { cwd: workDir, timeout: 30000 });
            return { name: step.name, status: 'SUCCESS' as const, output: (stdout + stderr).trim() || '[no output]', duration: Date.now() - start };
          } catch (err: any) {
            return { name: step.name, status: 'FAILED' as const, output: err.message, duration: Date.now() - start };
          }
        });

        const parallelResults = await Promise.all(promises);
        for (const r of parallelResults) {
          results.push(r);
          const icon = r.status === 'SUCCESS' ? '✅' : '❌';
          output += `${icon} ${r.name} (${r.duration}ms)\n`;
          if (r.output) output += `   ${r.output.substring(0, 200)}${r.output.length > 200 ? '...' : ''}\n`;
        }
      }

      const successCount = results.filter(r => r.status === 'SUCCESS').length;
      const failCount = results.filter(r => r.status === 'FAILED').length;
      const skipCount = results.filter(r => r.status === 'SKIPPED').length;

      output += `\n══════════════════════════\n`;
      output += `Results: ${successCount} passed, ${failCount} failed, ${skipCount} skipped\n`;
      output += `══════════════════════════`;

      return output.trim();
    } catch (err: any) {
      return `Pipeline error: ${err.message}`;
    }
  }
};
